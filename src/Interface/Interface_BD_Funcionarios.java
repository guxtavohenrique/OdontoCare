    private void listarValoresFuncionarios() {
        try {
            FuncionariosDAO funcionarios = new FuncionariosDAO();

            DefaultTableModel model = (DefaultTableModel) tblFuncionarios.getModel();
            model.setNumRows(0);

            ArrayList<Funcionarios> lista = funcionarios.Listar();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getNome(),
                    lista.get(num).getCpf(),
                    lista.get(num).getSexo(),
                    lista.get(num).getDat_nascimento(),
                    lista.get(num).getEmail(),
                    lista.get(num).getNum_celular(),
                    lista.get(num).getCargo(),
                    lista.get(num).getCro(),
                    lista.get(num).getUf(),
                    lista.get(num).getEspecializacao(),
                    lista.get(num).getCep(),
                    lista.get(num).getEndereco(),
                    lista.get(num).getComplemento(),
                    lista.get(num).getEstado(),
                    lista.get(num).getCidade(),
                    lista.get(num).getBairro()
                });
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao listar valores" + erro);
        }
    }
    
     private void excluirFuncionarios() {
        String nome;

        nome = txtCampo_pesquisar.getText();
        Funcionarios funcionarios = new Funcionarios();
        funcionarios.setNome(nome);

        FuncionariosDAO funcionariosdao = new FuncionariosDAO();
        funcionariosdao.excluirFuncionario(funcionarios);
    }
